package com.iteso.nintendo.items;

/**
 * Interfaz de poderes.
 */
public interface iPower {

    /**
     * Activacion de poder.
     */
    public void activatePower();

    /**
     * Lanzar hacia enfrente el poder.
     */
    public void frontTrow();

    /**
     * Lanzar hacia atras el poder.
     */
    public void backTrow();
}
