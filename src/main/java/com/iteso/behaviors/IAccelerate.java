package com.iteso.behaviors;
/**
 * Acceleration interface.
 */
public abstract class IAccelerate {
    /**
     * Getting acceleration.
     * @return end.
     */
    public abstract String getAccelerate();
    /**
     * Accelerating.
     * @return end.
     */
    public abstract String accelerate();
    /**
     * Decelerating.
     * @return end.
     */
    public abstract String decelerate();
}
