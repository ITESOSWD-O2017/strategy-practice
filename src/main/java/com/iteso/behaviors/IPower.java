package com.iteso.behaviors;
/**
 * Power interface.
 */
public abstract class IPower {
    /**
     * Get power.
     * @return end.
     */
    public abstract String getPower();
    /**
     * Catch power.
     * @return end.
     */
    public abstract String catchPower();
    /**
     * Prepare a power.
     *  @return end.
     */
    public abstract String preparePower();
    /**
     * Use a power.
     * @return end.
     */
    public abstract String usePower();
}
